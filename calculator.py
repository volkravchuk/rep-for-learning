#Basic Calculator

class Calculator:

    def add(num1, num2):
        """Add Function"""
        return num1 + num2

    def substract(num1, num2):
        """Substract Function"""
        return num1 - num2

    def multiply(num1, num2):
        """Multiply Function"""
        return num1 * num2

    def divide(num1, num2):
        """Divide( Function"""
        return num1 / num2